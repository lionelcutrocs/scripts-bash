#!/bin/bash



# Existen 3 bloques de operadores de comparacion,
# cada bloque pertene a un ambito diferente.


# Comparacion de Cadenas;

#  =   -> igual a
#  !=  -> no igual a 
#  >   -> mayor que (lexicograficamente)
#  <   -> menor que (lexicograficamente)

#------------------------------------------------

# Comparacion de Archivos;

#  -e   -> existe
#  -f   -> es un archivo regular
#  -d   -> es un directorio
#  -r   -> permite lectura
#  -w   -> permite escritura
#  -x   -> permite ejecutarse

#------------------------------------------------

# Comparacion de Numeros;

#  -eq  -> igual a
#  -ne  -> no igual a
#  -lt  -> menor que 
#  -le  -> menor o igual que
#  -gt  -> mayor que
#  -ge  -> mayor o igual que
